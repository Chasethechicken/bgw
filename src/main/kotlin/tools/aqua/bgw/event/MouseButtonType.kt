package tools.aqua.bgw.event

/**
 * Enum for all available mouse buttons.
 */
enum class MouseButtonType {
	/**
	 * LEFT / primary mouse button.
	 */
	LEFT_BUTTON,
	
	/**
	 * RIGHT / secondary mouse button.
	 */
	RIGHT_BUTTON,
	
	/**
	 * MOUSE WHEEL button.
	 */
	MOUSE_WHEEL,
	
	/**
	 * All other buttons.
	 */
	OTHER
}